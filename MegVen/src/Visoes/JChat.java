    private void envia()
    {
        if (!this.jTextArea2.getText().isEmpty())
        {
            this.conexao.envia(this.jTextArea2.getText());
            escreve("VocÃª disse: " + this.jTextArea2.getText());
            this.jTextArea2.setText("");
        }
    }

    private void escreve(String texto)
    {
        this.jTextArea1.append(texto + "\n");
    
        if ((!this.jTextArea1.getText().isEmpty()) && (!this.jTextArea1.isFocusOwner())) {
            this.jTextArea1.setCaretPosition(this.jTextArea1.getText().length() - 1);
        }
    }
    public void update(Observable o, Object arg)
    {
        escreve(this.conexao.getMensagem());
    }

