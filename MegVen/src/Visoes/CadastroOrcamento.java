        limpaTableOrcPE();
        DefaultTableModel model = (DefaultTableModel) gdOrcPe.getModel();
        OrcamentoPEDao orcPeDao = new OrcamentoPEDao();
        List<OrcamentoProdutoestoque> list = new ArrayList();            
